public class InfoMail {
    String Mail;
    String Name;
    String ItemName;

    public InfoMail(String mail, String name, String itemName) {
        Mail = mail;
        Name = name;
        ItemName = itemName;
    }

    public String getMail() {
        return Mail;
    }


    public String getName() {
        return Name;
    }


    public String getItemName() {
        return ItemName;
    }


}
